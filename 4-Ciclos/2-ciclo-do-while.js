// Ciclo do while
/**
 * El ciclo do while ejecuta al menos una vez su 
 * bloque de código debido a que la condición a revisar se
 * encuentra al final del bloque
 */

// Imprimir los valores del 0 a 5

let contador = 0, repeticiones = 5;
do {
    console.log(contador++);
} while (contador <= repeticiones)
